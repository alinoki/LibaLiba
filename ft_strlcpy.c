#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	if (dstsize == 0)
		return (ft_strlen((char *)src));
	while ((i < dstsize - 1) && *(char *)(src + i) != '\0')
	{
		*(dst + i) = *(char *)(src + i);
		i++;
	}
	*(dst + i) = '\0';
	return (ft_strlen((char *)src));
}
