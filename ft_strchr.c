#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	while (*(s + i) != '\0')
	{
		if (*(s + i) == (char) c)
			return (s + i);
		i++;
	}
	if ((char) c == '\0')
		return (s + i);
	return (NULL);
}
