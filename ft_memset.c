#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	if ((*(int *)s) == 0)
		write(1, "\0", 1);
	else if (!s)
		return (NULL);
	while (s && (i < n))
	{
		*(unsigned char *)(s + i) = (unsigned char)c;
		i++;
	}
	return (s);
}
